/*Palavras-chave JavaScript:

Palavras-chave JavaScript são usadas para 
identificar ações a serem executadas.

A palavra-chave 'let' diz ao navegador 
para criar variáveis:*/

let x, y;
x = 5 + 6;
y = x * 10;

/*A palavra-chave 'var' também diz ao navegador 
para criar variáveis:*/

var a, b;
a = 8 + 10;
b = a * 17;

console.log("Valor de x:", x, "e valor de y:", y)
console.log("Valor de a:", a, "e valor de b:", b)


