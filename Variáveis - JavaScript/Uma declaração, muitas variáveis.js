/*Você pode declarar muitas variáveis em um instrução.

Inicie a instrução 'let' w separe as variáveis por uma vírgula:*/

let pessoa1 = "Théo Bortoletto", carroNome1 = "Peugeot", preço1 = 17000;

/*Uma declaração pode abranger várias linhas:*/

let pessoa2 = "Flavia Sarahim", 
    carroNome2 = "Volkswagen", 
    preço2 = 60000;

