console.log("\n\tDeclarando uma variável - JavaScript:\n");

/*Criar uma variável em JavaScript é 
 de "declarar" uma variável.

Você declara uma variável JavaScript 
com var ou a  palavra-chave let:*/

var carName;
let carName;

/*Após a declaração, a variável não tem valor 
(tecnicamente é undefined - indefinida).

Para atribuir um valor à variável, use o sinal de igual:*/

carName = "Volvo"

/*Você também pode atribuir um valor à variável ao declará-la:*/

let CarName = "Volvo";
