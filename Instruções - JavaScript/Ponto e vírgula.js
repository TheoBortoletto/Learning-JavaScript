/*Ponto e Vírgula - ";"

Ponto e vírgula separam instruções JavaScript.

Adicione um ponto e vírgula no final de cada instrução executável:*/

let a, b, c; //* Declarando 3 variáveis

a = 10; //* Atribuir o valor de a para 10
b = 5;  //* Atribuir o valor de b para 5
c = a + b; //* Atribuir a soma de 'a' e 'b' pata a variável 'c'

console.log("Valor de a:", a);
console.log("Valor de b:", b);
console.log("Valor de c (a + b):", c)

console.log("\n\tInstrução em uma linha: \n")

/*Quando separados por ponto e vírgula, são permitidas várias instruções em uma linha:*/

let val1, val2, val3;

val1 = 1; val2 = 2; val3 = val1 + val2;

console.log("Valor de val1:", val1);
console.log("Valor de val2:", val2);
console.log("Valor de val3 (val1 + val2):", val3);

